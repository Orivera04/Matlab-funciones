function lh = pr_CreateList(fig,callbacks,ud,IL)
%PR_CREATELIST  Create an activeX listbox
%
% [lh,lyt,bdata] = pr_CreateList(fig,callbacks,ud,IL,bdata)

%  Copyright 2000-2004 The MathWorks, Inc. and Ford Global Technologies, Inc.

%  $Revision: 1.3.6.2 $    $Date: 2004/02/09 08:27:31 $

lPos = [-2 -2 1 1]; 
lh = actxcontainer(xregGui.listview(lPos , fig, callbacks));
invoke(lh,'InsertSmallIcons',IL);
set(lh , 'view' , 3, ...
    'multiselect', 0,...
    'labeledit' , 1, ...
    'hideselection' , 0,...
    'sorted' , 1, ...
    'userdata',ud, ...
    'Appearance', 0, ...
    'BorderStyle', 0, ...
    'FullRowSelect', 1, ...
    'Parent', double(fig));