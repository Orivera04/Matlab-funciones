%   m2xdate     - MATLAB 日付を EXCEL日付に変換
%   nweekdate   - 月の指定された平日の日付
%   second      - 指定した日または時間に対する時、分、秒の内の秒を表す
%   x2mdate     - EXCEL 日付を MATLAB 日付に変換
%   year        - 指定した日付に対する年、月、日の内の年を表す
%   yeardays    - 年を構成する日の数
