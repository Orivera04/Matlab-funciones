% CFCONV   キャッシュフローのコンベクシティ
%
% CX = CFCONV(CF,YLD)は、キャッシュフロー CF と定期利回り YLD が与えられ
% たときに、キャッシュフローのコンベクシティ C を計算します。
% 
% 例題：
% $2.50の支払い9回と$102.50の最終支払いのキャッシュフローで、定期利回り
% が2.5%の場合、コンベクシティ90.45期間が出力されます。
% 
% 参考 : BONDCONV, BONDDUR, CFDUR.


