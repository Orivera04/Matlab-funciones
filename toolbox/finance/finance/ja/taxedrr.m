% TAXEDRR   税引き後、収益率を出力
%
% R = TAXEDRR(RTN,TAX) は、名目収益率 RTN と税率 TAX に基づいて税引き後の
% 収益率を計算します。
% 
% 例題：
% ある投資の名目収益率が12%、税率が30%とします。この税引き後の収益率を求
% めます。
%        r = taxedrr(.12,.30) 
% この結果 r = 8.4%が出力されます。
%
% 参考 : EFFRR, IRR, MIRR, NOMRR, XIRR.


