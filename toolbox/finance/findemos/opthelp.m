'    years).',...
'.',...
'    Pull down the Simulation menu and choose Parameters.  In the Stop ',...
'    Time box, enter the number of time steps to be simulated.  (The ',...
'    default is 125, which represents 125 trading days out of the default ',...
'    250 per year).  Click OK to close and save.');
out5 = str2mat(...
'.',...
'    Double click the Start box to run the simulation with new parameters.',...
'.',...
' Click the Close button in the graphic window to close it.',...
'.',...
' Pull down the File menu and click Close to return to the Main Map.');
HelpDlgString = str2mat(out1,out2,out3,out4,out5);
