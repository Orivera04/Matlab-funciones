out1 = str2mat(...
' This demonstration shows how you can create and run mathematical models ',...
' using SIMULINK building blocks.  It uses Monte Carlo methods to value',...
' an option by modeling and simulating the path of a stock''s price over time.',...
'.',...
'    Double click the Start box to run the simulation.',...
'.',...
' A MATLAB graphic window shows the price path of each random walk as a ',...
' different colored line.  The simulation also returns and displays four ',...
' variables in the MATLAB workspace:');
out2 = str2mat(...
'.',...
'    fovalues = future stock price - exercise price',...
'    povalues = present value of fovalues',...
'    standev =  standard deviation of povalues',...
'    average =  mean of povalues',...
'.',...
' Average, the mean of povalues, is the fair option price for this run.',...
'.',...
' To change the simulation parameters:  Double click on a SIMULINK block ',...
' to open it; edit the parameters; click OK to close and save the changes.');
