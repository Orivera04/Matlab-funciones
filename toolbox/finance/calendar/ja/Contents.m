%   datefind    - 行列内の日付番号のインデックス
%   day         - 指定した月の日
%   eomdate     - 指定した月の末日
%   hour        - 指定した日または時間に対する時、分、秒の内の時を表す
%   lweekdate   - 指定した月の最後の平日の日付
%   minute      - 指定した日または時間に対する時、分、秒の内の分を表す
%   month       - 指定した日付に対する年、月、日の内の月を表す
