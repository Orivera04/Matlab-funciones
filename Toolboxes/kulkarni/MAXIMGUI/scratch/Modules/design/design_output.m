function design_output(design_problem, parameter_name, opt_parameter, opt_value)

global Scale  
% Output numerical results of a Design Model

designfig = figure('Name', 'Optimal Design', ...
                 'Color',[0.8 0.8 0.8], ...
                 'Units', 'normalized', ...
	              'Position', [.4 .4 .5 .5], ...
                 'Menubar', 'none', ...
                 'NumberTitle', 'off', ...
	              'Tag','designfig');

% Menu options for this figure
exit = uimenu(designfig, ...
              'Label', 'Close Figure',...
              'Tag','CloseFig', ...
              'Callback','close') ;
save = uimenu(designfig, ...
              'Label', 'Save Results', ...
              'Callback', sprintf('printto(''design'', ''file'') ;'), ...
              'Tag', 'SaveFig') ;
prnt = uimenu(designfig, ...
              'Label', 'Print Results', ...
              'Callback', sprintf('printto(''design'', ''printer'') ;'), ...
              'Tag', 'PrintFig') ;

% Text labels
uicontrol('Parent',designfig, ...
	       'Units','normalized', ...
          'Position',[.1 .85 .8 .10], ...
          'BackgroundColor',[0.8 0.8 0.8], ...
          'Fontsize', 14, ...
          'Style','text', ...
          'String', design_problem, ...
          'Tag','Title');
uicontrol('Parent',designfig, ...
	       'Units','normalized', ...
	       'Position',[.25 .65 .5 .08], ...
          'BackgroundColor',[0.8 0.8 0.8], ...
          'Fontsize', 12, ...
	       'Style','text', ...
          'String', sprintf('Optimal %s',parameter_name), ...
	       'Tag','OptParLabel');
uicontrol('Parent',designfig, ...
	       'Units','normalized', ...
	       'Position',[.25 .45 .5 .08], ...
          'BackgroundColor',[0.8 0.8 0.8], ...
          'Fontsize', 12, ...
	       'Style','text', ...
          'String', 'Optimal Cost Rate', ...
	       'Tag','OptValLabel');
% Results
% Optimal Parameter
uicontrol('Parent',designfig, ...
	       'Units','normalized', ...
	       'BackgroundColor',[1 1 1], ...
	       'Position',[.40 .55 .20 .09], ...
	       'Style','edit', ...
          'String', sprintf('%12.5f', opt_parameter), ...
	       'Tag','OptPar');
% Optimal Value
uicontrol('Parent',designfig, ...
	       'Units','normalized', ...
	       'BackgroundColor',[1 1 1], ...
	       'Position',[.4 .35 .2 .09], ...
	       'Style','edit', ...
          'String', sprintf('%12.5f', opt_value), ...
	       'Tag','OptVal');

