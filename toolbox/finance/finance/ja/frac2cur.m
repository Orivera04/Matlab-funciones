% FRAC2CUR   分数の通貨価値を10進数の価値に変換
%
% D = FRAC2CUR(P,F) は、分数の通貨価値を10進数の価値に変換します。P は、
% 文字列として入力される分数の通貨価値、F は分数の分母です。
%
% 例題：
%     d = frac2cur('12.1',8)は、d = 12.125を出力します。
% P は、文字列として入力してください。
% 
% 参考 : CUR2FRAC, CUR2STR.


