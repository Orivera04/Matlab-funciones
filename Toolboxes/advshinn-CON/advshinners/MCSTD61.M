function [ret,x0,str]=mcstd61(t,x,u,flag);
%MCSTD61	is the M-file description of the SIMULINK system named MCSTD61.
%	The block-diagram can be displayed by typing: MCSTD61.
%
%	SYS=MCSTD61(T,X,U,FLAG) returns depending on FLAG certain
%	system values given time point, T, current state vector, X,
%	and input vector, U.
%	FLAG is used to indicate the type of output to be returned in SYS.
%
%	Setting FLAG=1 causes MCSTD61 to return state derivatives, FLAG=2
%	discrete states, FLAG=3 system outputs and FLAG=4 next sample
%	time. For more information and other options see SFUNC.
%
%	Calling MCSTD61 with a FLAG of zero:
%	[SIZES]=MCSTD61([],[],[],0),  returns a vector, SIZES, which
%	contains the sizes of the state vector and other parameters.
%		SIZES(1) number of states
%		SIZES(2) number of discrete states
%		SIZES(3) number of outputs
%		SIZES(4) number of inputs.
%	For the definition of other parameters in SIZES, see SFUNC.
%	See also, TRIM, LINMOD, LINSIM, EULER, RK23, RK45, ADAMS, GEAR.

% Note: This M-file is only used for saving graphical information;
%       after the model is loaded into memory an internal model
%       representation is used.

% the system will take on the name of this mfile:
sys = mfilename;
new_system(sys)
simver(1.2)
if(0 == (nargin + nargout))
     set_param(sys,'Location',[7,53,437,280])
     open_system(sys)
end;
set_param(sys,'algorithm',		'RK-45')
set_param(sys,'Start time',	'0.0')
set_param(sys,'Stop time',		'20')
set_param(sys,'Min step size',	'0.0001')
set_param(sys,'Max step size',	'.05')
set_param(sys,'Relative error','1e-3')
set_param(sys,'Return vars',	'')

add_block('built-in/Step Fcn',[sys,'/','Step Fcn'])
set_param([sys,'/','Step Fcn'],...
		'Time','0',...
		'position',[25,20,45,40])

add_block('built-in/Sum',[sys,'/','Sum'])
set_param([sys,'/','Sum'],...
		'inputs','+-',...
		'position',[90,25,110,45])

add_block('built-in/Discrete Zero-Pole',[sys,'/',['Discrete Lead Network',13,'D(z)']])
set_param([sys,'/',['Discrete Lead Network',13,'D(z)']],...
		'Poles','.152524',...
		'position',[140,18,200,52])

add_block('built-in/Discrete Zero-Pole',[sys,'/',['Process',13,'G(z)']])
set_param([sys,'/',['Process',13,'G(z)']],...
		'Zeros','[-.5]',...
		'Poles','[1;1]',...
		'Gain','.2785',...
		'position',[230,18,290,52])

add_block('built-in/Scope',[sys,'/','C(s)'])
set_param([sys,'/','C(s)'],...
		'Vgain','1.500000',...
		'Hgain','20.000000',...
		'Vmax','3.000000',...
		'Hmax','40.000000',...
		'Window',[458,49,640,280])
open_system([sys,'/','C(s)'])
set_param([sys,'/','C(s)'],...
		'position',[370,22,390,48])

add_block('built-in/Clock',[sys,'/','Clock'])
set_param([sys,'/','Clock'],...
		'position',[15,175,35,195])

add_block('built-in/Look Up Table',[sys,'/','Look Up Table'])
set_param([sys,'/','Look Up Table'],...
		'Input_Values','[0:20]',...
		'Output_Values','[0 .5 1 .5 0 zeros(1,16)]',...
		'position',[70,175,95,195])

add_block('built-in/Discrete Transfer Fcn',[sys,'/','Zero Order Hold'])
set_param([sys,'/','Zero Order Hold'],...
		'Numerator','1',...
		'Denominator','1',...
		'Sample time','Ts',...
		'Mask Display','plot(0,0,100,100,[90,70,70,50,50,30,30,10],[30,30,50,50,80,80,20,20])',...
		'Mask Type','Zero Order Hold')
set_param([sys,'/','Zero Order Hold'],...
		'Mask Dialogue','Zero Order Hold|Sample Time:',...
		'Mask Translate','Ts=@1;',...
		'Mask Help','Implements a sample-and-hold latch operating at the sampling interval you specify.')
set_param([sys,'/','Zero Order Hold'],...
		'Mask Entries','1\/',...
		'position',[135,169,165,201])

add_block('built-in/Transfer Fcn',[sys,'/','G(s)'])
set_param([sys,'/','G(s)'],...
		'Denominator','[1 0.5]',...
		'position',[210,167,245,203])

add_block('built-in/Discrete Transfer Fcn',[sys,'/','Zero Order Hold1'])
set_param([sys,'/','Zero Order Hold1'],...
		'Numerator','1',...
		'Denominator','1',...
		'Sample time','Ts',...
		'Mask Display','plot(0,0,100,100,[90,70,70,50,50,30,30,10],[30,30,50,50,80,80,20,20])',...
		'Mask Type','Zero Order Hold')
set_param([sys,'/','Zero Order Hold1'],...
		'Mask Dialogue','Zero Order Hold|Sample Time:',...
		'Mask Translate','Ts=@1;',...
		'Mask Help','Implements a sample-and-hold latch operating at the sampling interval you specify.')
set_param([sys,'/','Zero Order Hold1'],...
		'Mask Entries','1\/',...
		'position',[310,129,340,161])

add_block('built-in/Scope',[sys,'/','c(t)'])
set_param([sys,'/','c(t)'],...
		'Vgain','1.500000',...
		'Hgain','20.000000',...
		'Vmax','3.000000',...
		'Hmax','40.000000',...
		'Window',[52,299,288,464])
open_system([sys,'/','c(t)'])
set_param([sys,'/','c(t)'],...
		'position',[400,172,420,198])

add_block('built-in/Scope',[sys,'/','c*(t)'])
set_param([sys,'/','c*(t)'],...
		'Vgain','1.500000',...
		'Hgain','20.000000',...
		'Vmax','3.000000',...
		'Hmax','40.000000',...
		'Window',[304,298,541,466])
open_system([sys,'/','c*(t)'])
set_param([sys,'/','c*(t)'],...
		'position',[400,132,420,158])
add_line(sys,[50,30;80,30])
add_line(sys,[115,35;130,35])
add_line(sys,[205,35;220,35])
add_line(sys,[295,35;360,35])
add_line(sys,[295,35;320,35;320,105;65,105;65,40;80,40])
add_line(sys,[40,185;60,185])
add_line(sys,[100,185;125,185])
add_line(sys,[170,185;200,185])
add_line(sys,[250,185;390,185])
add_line(sys,[250,185;275,185;275,145;300,145])
add_line(sys,[345,145;390,145])

% Return any arguments.
if (nargin | nargout)
	% Must use feval here to access system in memory
	if (nargin > 3)
		if (flag == 0)
			eval(['[ret,x0,str]=',sys,'(t,x,u,flag);'])
		else
			eval(['ret =', sys,'(t,x,u,flag);'])
		end
	else
		[ret,x0,str] = feval(sys);
	end
end
