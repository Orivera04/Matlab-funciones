% 金融日付
%   busdate     - つぎの営業日または前の営業日
%   datemnth    - 将来の月または過去の月における日の日付
%   datewrkdy   - 将来または過去の営業日の日付
%   days360     - 1年＝360日として計算された日付間の日数
%   days365     - 1年＝365日として計算された日付間の日数
%   daysdif     - ある日付カウント基準で計算された日付間の日数
%   daysact     - 現実の年に基づいて計算された日付間の日数
%   fbusdate    - 指定した月の最初の営業日
%   holidays    - 休日及び休業日
%   isbusday    - 指定された日付が営業日の場合、真を出力
%   lbusdate    - 月の最後の営業日
%   wrkdydif    - 指定した日付間の営業日数
%   yearfrac    - 指定した日付間の年の端数
%   
% クーポン債日付
