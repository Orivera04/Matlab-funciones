%   cpndatep    - 指定した日付より前に到来した前回クーポン支払日
%   cpndatepq   - 指定した日付より前に到来した準クーポン支払日
%   cpndaysn    - 指定した日付と次回クーポン支払日間の日数
%   cpndaysp    - 指定した日付と前回クーポン支払日間の日数
%   cpnpersz    - 指定した日付を含む期間の日数の長さ


